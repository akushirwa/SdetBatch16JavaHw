package ArraysHW;

public class ArraysHw6 {
    public static void main(String[] args) {


        int [] numbers={4,12,5,7,2,4};
        int sum=0;

        for (int i = 0; i < numbers.length; i++) {
            sum+=numbers[i];
        }

        System.out.println("The sum of the array is "+sum);






    }
}
